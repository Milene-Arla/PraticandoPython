# Faça uma função que recebe uma idade e, se for maior que 18 anos, exibe na tela "Você é maior de idade", e se não, exibe "Você é menor de idade".

def maior_idade(idade):
    if idade >= 18:
        print('Você é maior de idade')
    else:
        print('Você é menor de idade')     


maior_idade(18)
